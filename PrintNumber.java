public class PrintNumber {
    public static void main(String[] args) {
        int n=10;
        int k=20;

        System.out.println("Even Number: ");
        for(int i=0;i<=n;i+=2){   
            System.out.print(i +" " );
        } 
        System.out.println();
        System.out.println("Odd Number: ");
        for(int j=1; j<=k ;j+=2){
            System.out.print( " "+j);
        }
    }
    
}
